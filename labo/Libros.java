public class Libros {
    private int id;
    private float precio;
    private String nombre;
    private String editorial;

    public int getId() {
        return id;
    }

    public float getPrecio() {
        return precio;
    }

    public String getNombre() {
        return nombre;
    }

    public String getEditorial() {
        return editorial;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }
}